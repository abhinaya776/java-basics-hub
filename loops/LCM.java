import java.util.Scanner;
public class Main
{
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		  int a=sc.nextInt();
		  int b=sc.nextInt(); 
		  int max=(a>b)?a:b; int lcm=max;
		  while(true){
		       if(lcm%a==0 && lcm%b==0){
		           break;
		       }
		       lcm=lcm+max;
		  }
		  System.out.println("LCM " +lcm);
		  sc.close();
	}
}
